#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*d;

	i = 0;
	if (!(d = (char *)malloc(sizeof(char) * (ft_strlen(s1) + 1))))
		return (NULL);
	while (s1[i] != '\0')
	{
		d[i] = s1[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}
